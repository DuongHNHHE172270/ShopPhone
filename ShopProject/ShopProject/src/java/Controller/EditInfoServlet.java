    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String uid = request.getParameter("uid");
        DBUsers dbu = new DBUsers();
        int userId = Integer.parseInt(uid);
        Users u = dbu.getUserId(userId);
        request.setAttribute("u", u);
        request.setAttribute("uid", uid);
        request.getRequestDispatcher("editinfo.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String userId = request.getParameter("userId");
        String role = request.getParameter("role");
        String action = request.getParameter("action");
        String comfirm = request.getParameter("comfirm");
        DBUsers dbu = new DBUsers();

        if (action.equals("update")) {
            if (comfirm != null && comfirm.equals("true")) {
                if (userId != null && role != null) {
                    int userid = Integer.parseInt(userId);
                    int role_change = Integer.parseInt(role);
                    
                   dbu.updateUserRole(role_change, userid);
                   
                }
            }
        }
       
      
        response.sendRedirect("manager");
    }

